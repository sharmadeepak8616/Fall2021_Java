package Class20;

public class GitCommands {
    /**
     *
     * To view local branches
     *  git branch
     *
     * To create a new branch
     *  git branch branchName
     *
     * To push a branch into remote
     *  git push -u origin branchName
     *
     * To move/switch to a branch
     *  git checkout branchName
     *
     * To view the difference between local-branch and remote-branch
     *  git status
     *
     * To delete a branch in local
     *  git branch -d branchName
     */
}
